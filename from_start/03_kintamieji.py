# Sukurkite kintamąjį a ir priskirkite jam bet kokį sveikąjį skaičių (int)
a = int(input("iveskite norima skaiciu: "))

# Sukurkite kitą kintamąjį b ir priskirkite jam bet kokį slankiojo kablelio skaičių. (float)
b = float(input("Iveskite norima skaiciu su kableliu: "))

# Tada sukurkite trečiajį kintamajį c ir priskirkite jam bet kokį kompleksinį skaičių (complex)
c = complex(input("iveskite kompleksini skaiciu: "))

# Atspausdinkite visus tris kintamuosius ir patikrinkite jų tipus
print(a, type(a))
print(b, type(b))
print(c, type(c))